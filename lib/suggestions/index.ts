export function filterSuggestions(query: string, list: string[], maxResults = 8): string[] {
  if (!query.trim()) return list.slice(0, maxResults)
  const q = query.toLowerCase()
  const starts = list.filter(item => item.toLowerCase().startsWith(q))
  const contains = list.filter(item => !item.toLowerCase().startsWith(q) && item.toLowerCase().includes(q))
  return [...starts, ...contains].slice(0, maxResults)
}
